def day(status):
    match status:
        case 1 : return "Monday"
        case 2  : return "tuesday" 
        case 3 : return "Wednesday"
        case 4  : return "thursday" 
        case 5 : return "friday"
        case 6  : return "saturday" 
        case 7 : return "sonday"
        
print(day(1))



        
        